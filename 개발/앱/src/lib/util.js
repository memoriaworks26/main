// 2자리 0패딩 ("9" → "09"). 시:분·날짜 조합에 공용.
export const pad2 = (n) => String(n).padStart(2, "0");

// 목록 텍스트 검색 술어 — q가 비면 통과, 아니면 필드를 공백으로 이어 부분일치(대소문자 무시).
// nullish 필드는 ""로 강등(기존 각 화면의 (x||"") 가드와 동일 동작).
export const matchQuery = (q, ...fields) => {
  const s = q.trim().toLowerCase();
  return !s || fields.map((f) => f ?? "").join(" ").toLowerCase().includes(s);
};
